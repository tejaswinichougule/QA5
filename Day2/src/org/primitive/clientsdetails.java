package org.primitive;

public class clientsdetails {
	
	public static void main(String[] args) {
		
		//byte- 2 digit
		byte age=25;
		System.out.println(age);
		System.out.println("age val is"+age);
		
		// short- 4 digit
		short empId=1234;
		System.out.println(empId);
		System.out.println("empId  is"+empId);
		
		// int- 9 digit
		int regNo=123456;
		System.out.println(regNo);
		System.out.println("regno is"+regNo);
		
		// long >9 digits
		long phNo=135654434;
		System.out.println(phNo);
		System.out.println("phno is "+phNo);
		
		//float
		float avg=98.67f;
		System.out.println(avg);
		System.out.println("avg is"+avg);
		
		//double
		double salary=655657.76;
		System.out.println(salary);
		System.out.println("salary is"+salary);
		
		//char
		char blkname='c';
		System.out.println("block name "+blkname);
		
		//boolean
		boolean empstatus=true;
		System.out.println("emp status"+empstatus);
		
		//string
		String em="java@gmail.com";
		System.out.println("mail id"+em);
		
		
	}

}
